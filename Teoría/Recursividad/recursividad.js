/*	FACTORIAL
	5! => 5 * 4 * 3 * 2 * 1 = 120
*/

function factor(n) {

	if (n == 1) 
		return 1;
	else
		return n * factor(n - 1);
}

factor(5);

/*----------------------------------------------------------------------------------------------*/

/*	FIBONACCI
	1 1 2 3 5 8 13 21...
*/

function fibo(n) {
	if (n == 1 || n == 2)
		return 1;
	else
		return fibo(n - 1) + fibo(n - 2);
}

fibo(5);

